import React, { useState, useEffect } from 'react';
import { Link } from 'react-router-dom';
import axios from 'axios';
import '../../styles/UserManagement.css';

const UserManagement = () => {
  const [users, setUsers] = useState([]);
  const [loading, setLoading] = useState(true);
  const [searchTerm, setSearchTerm] = useState('');
  const [roleFilter, setRoleFilter] = useState('all');
  const [sortBy, setSortBy] = useState('name');
  const [stats, setStats] = useState({
    total: 0,
    active: 0,
    admin: 0,
    today: 0
  });

  useEffect(() => {
    fetchUsers();
  }, []);

  const fetchUsers = async () => {
    try {
      setLoading(true);
      
      // Get auth token
      const token = localStorage.getItem('token');
      if (!token) {
        console.error('No auth token found');
        return;
      }

      const config = {
        headers: {
          'Authorization': `Bearer ${token}`,
          'Content-Type': 'application/json'
        }
      };

      const response = await axios.get('http://localhost:5000/api/users', config);
      
      if (response.data.success) {
        const usersData = response.data.data;
        setUsers(usersData);
        
        // Calculate stats
        const today = new Date().toDateString();
        setStats({
          total: usersData.length,
          active: usersData.filter(user => user.isActive !== false).length, // Default to active if not specified
          admin: usersData.filter(user => user.role === 'admin').length,
          today: usersData.filter(user => 
            new Date(user.createdAt).toDateString() === today
          ).length
        });
      }
    } catch (error) {
      console.error('Error fetching users:', error);
      
      if (error.response) {
        if (error.response.status === 401) {
          alert('Phi√™n ƒëƒÉng nh·∫≠p ƒë√£ h·∫øt h·∫°n. Vui l√≤ng ƒëƒÉng nh·∫≠p l·∫°i.');
          localStorage.removeItem('token');
          window.location.href = '/login';
        } else if (error.response.status === 403) {
          alert('B·∫°n kh√¥ng c√≥ quy·ªÅn truy c·∫≠p trang n√†y.');
        } else {
          alert(`L·ªói: ${error.response.data?.message || 'Kh√¥ng th·ªÉ t·∫£i d·ªØ li·ªáu ng∆∞·ªùi d√πng'}`);
        }
      } else {
        alert('L·ªói k·∫øt n·ªëi. Vui l√≤ng ki·ªÉm tra backend server.');
      }
    } finally {
      setLoading(false);
    }
  };

  const formatDate = (dateString) => {
    return new Date(dateString).toLocaleDateString('vi-VN');
  };

  const handleToggleUserStatus = async (userId, currentStatus) => {
    try {
      const token = localStorage.getItem('token');
      const config = {
        headers: {
          'Authorization': `Bearer ${token}`,
          'Content-Type': 'application/json'
        }
      };

      const newStatus = !currentStatus;
      await axios.put(
        `http://localhost:5000/api/users/${userId}/status`,
        { isActive: newStatus },
        config
      );

      // Refresh user list
      fetchUsers();
      alert(`ƒê√£ ${newStatus ? 'k√≠ch ho·∫°t' : 'v√¥ hi·ªáu h√≥a'} ng∆∞·ªùi d√πng`);
    } catch (error) {
      console.error('Error updating user status:', error);
      alert('L·ªói khi c·∫≠p nh·∫≠t tr·∫°ng th√°i ng∆∞·ªùi d√πng');
    }
  };

  const handleDeleteUser = async (userId, userName) => {
    if (!window.confirm(`B·∫°n c√≥ ch·∫Øc ch·∫Øn mu·ªën x√≥a ng∆∞·ªùi d√πng "${userName}"?`)) {
      return;
    }

    try {
      const token = localStorage.getItem('token');
      const config = {
        headers: {
          'Authorization': `Bearer ${token}`,
          'Content-Type': 'application/json'
        }
      };

      await axios.delete(`http://localhost:5000/api/users/${userId}`, config);
      
      // Refresh user list
      fetchUsers();
      alert('ƒê√£ x√≥a ng∆∞·ªùi d√πng th√†nh c√¥ng');
    } catch (error) {
      console.error('Error deleting user:', error);
      if (error.response?.data?.message) {
        alert(`L·ªói: ${error.response.data.message}`);
      } else {
        alert('L·ªói khi x√≥a ng∆∞·ªùi d√πng');
      }
    }
  };

  // Filter and sort users
  const filteredUsers = users
    .filter(user => {
      // Search by name or email
      const matchesSearch = user.name?.toLowerCase().includes(searchTerm.toLowerCase()) ||
                           user.email?.toLowerCase().includes(searchTerm.toLowerCase());
      
      // Filter by role
      const matchesRole = roleFilter === 'all' || user.role === roleFilter;
      
      return matchesSearch && matchesRole;
    })
    .sort((a, b) => {
      switch(sortBy) {
        case 'name':
          return (a.name || '').localeCompare(b.name || '');
        case 'name-desc':
          return (b.name || '').localeCompare(a.name || '');
        case 'email':
          return (a.email || '').localeCompare(b.email || '');
        case 'date':
          return new Date(b.createdAt) - new Date(a.createdAt);
        default:
          return 0;
      }
    });

  if (loading) {
    return (
      <div className="user-management">
        <div className="loading">ƒêang t·∫£i d·ªØ li·ªáu ng∆∞·ªùi d√πng...</div>
      </div>
    );
  }

  return (
    <div className="drink-management-container">
      {/* Header */}
      <div className="management-header">
        <div className="header-content">
          <h1>üë• Qu·∫£n l√Ω ng∆∞·ªùi d√πng</h1>
          {/* ƒê√£ xo√° n√∫t Th√™m ng∆∞·ªùi d√πng */}
        </div>
        <div style={{ 
          marginTop: '1rem', 
          padding: '15px 0',
          borderTop: '1px solid #eee'
        }}>
          <Link 
            to="/admin" 
            style={{
              background: '#dc3545',
              color: 'white',
              padding: '10px 20px',
              borderRadius: '8px',
              textDecoration: 'none',
              fontWeight: '600',
              display: 'inline-flex',
              alignItems: 'center',
              gap: '8px'
            }}
          >
            ‚Üê Quay l·∫°i Dashboard
          </Link>
        </div>
      </div>

      {/* Stats */}
      <div className="stats-grid">
        <div className="stat-card">
          <div className="stat-icon">üë§</div>
          <div className="stat-info">
            <h3>{stats.total}</h3>
            <p>T·ªïng ng∆∞·ªùi d√πng</p>
          </div>
        </div>
        
        <div className="stat-card">
          <div className="stat-icon">‚úÖ</div>
          <div className="stat-info">
            <h3>{stats.active}</h3>
            <p>Ho·∫°t ƒë·ªông</p>
          </div>
        </div>
        
        <div className="stat-card">
          <div className="stat-icon">‚ö°</div>
          <div className="stat-info">
            <h3>{stats.admin}</h3>
            <p>Qu·∫£n tr·ªã vi√™n</p>
          </div>
        </div>
        
        <div className="stat-card">
          <div className="stat-icon">üìÖ</div>
          <div className="stat-info">
            <h3>{stats.today}</h3>
            <p>ƒêƒÉng k√Ω h√¥m nay</p>
          </div>
        </div>
      </div>

      {/* Filters */}
      <div className="filters-section">
        <div className="filters-row">
          <div className="filter-group">
            <input 
              type="text" 
              placeholder="T√¨m ki·∫øm ng∆∞·ªùi d√πng..." 
              className="search-input"
              value={searchTerm}
              onChange={(e) => setSearchTerm(e.target.value)}
            />
          </div>
          <div className="filter-group">
            <select 
              className="filter-select"
              value={roleFilter}
              onChange={(e) => setRoleFilter(e.target.value)}
            >
              <option value="all">T·∫•t c·∫£ vai tr√≤</option>
              <option value="admin">Qu·∫£n tr·ªã vi√™n</option>
              <option value="user">Ng∆∞·ªùi d√πng</option>
            </select>
          </div>
          <div className="filter-group">
            <select 
              className="filter-select"
              value={sortBy}
              onChange={(e) => setSortBy(e.target.value)}
            >
              <option value="name">A-Z</option>
              <option value="name-desc">Z-A</option>
              <option value="email">Theo email</option>
              <option value="date">M·ªõi nh·∫•t</option>
            </select>
          </div>
        </div>
      </div>

      {/* Users Table */}
      <div className="users-section">
        <div className="section-header">
          <h2>Danh s√°ch ng∆∞·ªùi d√πng</h2>
        </div>
        
        <div className="users-table-container">
          <table className="users-table">
            <thead>
              <tr>
                <th>Avatar</th>
                <th>T√™n</th>
                <th>Email</th>
                <th>Vai tr√≤</th>
                <th>Tr·∫°ng th√°i</th>
                <th>Ng√†y tham gia</th>
                <th>Thao t√°c</th>
              </tr>
            </thead>
            <tbody>
              {filteredUsers.map(user => (
                <tr key={user._id}>
                  <td>
                    <div className="user-avatar">
                      {user.profilePicture ? (
                        <img src={user.profilePicture} alt={user.name} />
                      ) : (
                        <div className="avatar-placeholder">
                          {user.name?.charAt(0).toUpperCase() || '?'}
                        </div>
                      )}
                    </div>
                  </td>
                  <td>
                    <div className="user-name">
                      <strong>{user.name || 'Ch∆∞a c√≥ t√™n'}</strong>
                    </div>
                  </td>
                  <td>{user.email}</td>
                  <td>
                    <span className={`role-badge ${user.role}`}>
                      {user.role === 'admin' ? 'üëë Admin' : 'üë§ User'}
                    </span>
                  </td>
                  <td>
                    <span className={`status-badge ${user.isActive !== false ? 'active' : 'inactive'}`}>
                      {user.isActive !== false ? 'üü¢ Ho·∫°t ƒë·ªông' : 'üî¥ Kh√¥ng ho·∫°t ƒë·ªông'}
                    </span>
                  </td>
                  <td>{formatDate(user.createdAt)}</td>
                  <td>
                    <div className="action-buttons">
                      <button 
                        className="btn-toggle" 
                        title={user.isActive !== false ? 'V√¥ hi·ªáu h√≥a' : 'K√≠ch ho·∫°t'}
                        onClick={() => handleToggleUserStatus(user._id, user.isActive !== false)}
                      >
                        {user.isActive !== false ? 'üî¥' : 'üü¢'}
                      </button>
                      <button 
                        className="btn-delete" 
                        title="X√≥a"
                        onClick={() => handleDeleteUser(user._id, user.name || user.email)}
                      >
                        üóëÔ∏è
                      </button>
                    </div>
                  </td>
                </tr>
              ))}
            </tbody>
          </table>
          
          {users.length === 0 && (
            <div className="empty-state">
              <p>Ch∆∞a c√≥ ng∆∞·ªùi d√πng n√†o</p>
            </div>
          )}
        </div>
      </div>
    </div>
  );
};

export default UserManagement;
