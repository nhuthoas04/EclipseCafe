import React, { useState, useEffect } from 'react';
import { Link, useLocation } from 'react-router-dom';
import axios from 'axios';
import '../../styles/DrinkManagement.css';

const DrinkManagement = () => {
  const location = useLocation();
  const [drinks, setDrinks] = useState([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState('');
  const [stats, setStats] = useState({
    total: 0,
    available: 0,
    outOfStock: 0
  });
  
  // Get filter from URL params
  const urlParams = new URLSearchParams(location.search);
  const filterParam = urlParams.get('filter');
  
  const [filters, setFilters] = useState({
    search: '',
    category: '',
    isAvailable: filterParam === 'available' ? 'true' : ''
  });
  const [pagination, setPagination] = useState({
    page: 1,
    limit: 10,
    total: 0,
    pages: 0
  });

  const categories = {
    '': 'T·∫•t c·∫£',
    coffee: 'C√† ph√™',
    'a-me': 'A-M√™',
    'hi-tea': 'Hi-Tea',
    matcha: 'Matcha',
    cake: 'B√°nh m·∫∑n v√† ng·ªçt'
  };

  // Check user role on component mount
  useEffect(() => {
    const checkUserRole = () => {
      const token = localStorage.getItem('token');
      const userStr = localStorage.getItem('user');
      
      console.log('Token exists:', !!token);
      if (userStr) {
        try {
          const user = JSON.parse(userStr);
          console.log('User role:', user.role);
          if (user.role !== 'admin') {
            alert('B·∫°n kh√¥ng c√≥ quy·ªÅn truy c·∫≠p trang n√†y. Vui l√≤ng ƒëƒÉng nh·∫≠p v·ªõi t√†i kho·∫£n admin.');
          }
        } catch (e) {
          console.error('Error parsing user data:', e);
        }
      } else {
        console.log('No user data found');
      }
    };
    
    checkUserRole();
  }, []);

  // Update filters when URL changes
  useEffect(() => {
    const urlParams = new URLSearchParams(location.search);
    const filterParam = urlParams.get('filter');
    
    if (filterParam === 'available') {
      setFilters(prev => ({
        ...prev,
        isAvailable: 'true'
      }));
    }
  }, [location.search]);

  useEffect(() => {
    fetchDrinks();
  }, [pagination.page, filters.category, filters.isAvailable]);

  // Debounce search to avoid too many API calls
  useEffect(() => {
    const timeoutId = setTimeout(() => {
      fetchDrinks();
    }, 500);

    return () => clearTimeout(timeoutId);
  }, [filters.search]);

  const fetchDrinks = async () => {
    try {
      setLoading(true);
      
      // Prepare query parameters
      const queryParams = new URLSearchParams({
        page: pagination.page.toString(),
        limit: pagination.limit.toString()
      });

      // Add search parameter
      if (filters.search.trim()) {
        queryParams.append('search', filters.search.trim());
      }

      // Add category filter
      if (filters.category) {
        queryParams.append('category', filters.category);
      }

      // Add availability filter (convert isAvailable to available)
      if (filters.isAvailable) {
        queryParams.append('available', filters.isAvailable);
      }

      console.log('Fetching drinks with params:', queryParams.toString());
      const response = await axios.get(`http://localhost:5000/api/drinks?${queryParams}`);
      
      if (response.data.success) {
        const drinksData = response.data.data;
        setDrinks(drinksData);
        setPagination(prev => ({
          ...prev,
          total: response.data.pagination.total,
          pages: response.data.pagination.pages
        }));

        // Calculate stats
        setStats({
          total: drinksData.length,
          available: drinksData.filter(drink => drink.isAvailable !== false).length,
          outOfStock: drinksData.filter(drink => drink.isAvailable === false).length
        });
      }
    } catch (error) {
      console.error('Error fetching drinks:', error);
      
      if (error.code === 'ECONNREFUSED' || error.message.includes('Network Error')) {
        setError('‚ùå Kh√¥ng th·ªÉ k·∫øt n·ªëi v·ªõi server backend. Vui l√≤ng ki·ªÉm tra backend server c√≥ ƒëang ch·∫°y tr√™n port 5000?');
      } else if (error.response) {
        // Server responded with error status
        const status = error.response.status;
        const message = error.response.data?.message || 'Unknown error';
        setError(`‚ùå L·ªói server (${status}): ${message}`);
      } else {
        setError('‚ùå C√≥ l·ªói x·∫£y ra khi t·∫£i danh s√°ch ƒë·ªì u·ªëng. Vui l√≤ng th·ª≠ l·∫°i.');
      }
    } finally {
      setLoading(false);
    }
  };

  const handleFilterChange = (e) => {
    const { name, value } = e.target;
    setFilters(prev => ({
      ...prev,
      [name]: value
    }));
    setPagination(prev => ({
      ...prev,
      page: 1
    }));
  };

  const handlePageChange = (newPage) => {
    setPagination(prev => ({
      ...prev,
      page: newPage
    }));
  };

  const handleDeleteDrink = async (drinkId, drinkName) => {
    if (window.confirm(`B·∫°n c√≥ ch·∫Øc ch·∫Øn mu·ªën x√≥a ƒë·ªì u·ªëng "${drinkName}"?`)) {
      try {
        await axios.delete(`http://localhost:5000/api/drinks/${drinkId}`);
        fetchDrinks(); // Reload list
        alert('X√≥a ƒë·ªì u·ªëng th√†nh c√¥ng!');
      } catch (error) {
        alert('C√≥ l·ªói x·∫£y ra khi x√≥a ƒë·ªì u·ªëng');
        console.error('Error deleting drink:', error);
      }
    }
  };

  const toggleAvailability = async (drinkId, currentStatus) => {
    try {
      await axios.put(`http://localhost:5000/api/drinks/${drinkId}`, {
        isAvailable: !currentStatus
      });
      fetchDrinks(); // Reload list
    } catch (error) {
      alert('C√≥ l·ªói x·∫£y ra khi c·∫≠p nh·∫≠t tr·∫°ng th√°i');
      console.error('Error updating availability:', error);
    }
  };

  const toggleFeatured = async (drinkId, currentStatus) => {
    try {
      const token = localStorage.getItem('token');
      if (!token) {
        alert('Vui l√≤ng ƒëƒÉng nh·∫≠p ƒë·ªÉ th·ª±c hi·ªán thao t√°c n√†y');
        return;
      }
      
      const response = await axios.patch(`http://localhost:5000/api/drinks/${drinkId}/featured`, {}, {
        headers: {
          Authorization: `Bearer ${token}`
        }
      });
      fetchDrinks(); // Reload list
      alert(response.data.message || (currentStatus ? 'ƒê√£ b·ªè ghim s·∫£n ph·∫©m' : 'ƒê√£ ghim s·∫£n ph·∫©m v√†o trang ch·ªß'));
    } catch (error) {
      console.error('Error updating featured status:', error);
      const errorMessage = error.response?.data?.message || 'C√≥ l·ªói x·∫£y ra khi c·∫≠p nh·∫≠t tr·∫°ng th√°i ghim';
      alert(errorMessage);
    }
  };

  const formatPrice = (price) => {
    return new Intl.NumberFormat('vi-VN', {
      style: 'currency',
      currency: 'VND'
    }).format(price);
  };

  const formatDate = (date) => {
    return new Date(date).toLocaleDateString('vi-VN');
  };

  if (loading) {
    return (
      <div className="drink-management-container">
        <div className="loading">ƒêang t·∫£i danh s√°ch ƒë·ªì u·ªëng...</div>
      </div>
    );
  }

  return (
    <div className="drink-management-container">
      <div className="management-header">
        <div className="header-content">
          <h1>ü•§ Qu·∫£n l√Ω ƒë·ªì u·ªëng</h1>
          <div className="header-actions">
            <Link to="/admin/drinks/add" className="add-btn">
              ‚ûï Th√™m ƒë·ªì u·ªëng m·ªõi
            </Link>
          </div>
        </div>
        <div style={{ 
          marginTop: '1rem', 
          padding: '15px 0',
          borderTop: '1px solid #eee'
        }}>
          <Link 
            to="/admin" 
            style={{
              background: '#dc3545',
              color: 'white',
              padding: '10px 20px',
              borderRadius: '8px',
              textDecoration: 'none',
              fontWeight: '600',
              display: 'inline-flex',
              alignItems: 'center',
              gap: '8px'
            }}
          >
            ‚Üê Quay l·∫°i Dashboard
          </Link>
        </div>
      </div>

      {/* Stats */}
      <div className="stats-grid">
        <div className="stat-card">
          <div className="stat-icon">ü•§</div>
          <div className="stat-info">
            <h3>{stats.total}</h3>
            <p>T·ªïng ƒë·ªì u·ªëng</p>
          </div>
        </div>
        
        <div className="stat-card">
          <div className="stat-icon">‚úÖ</div>
          <div className="stat-info">
            <h3>{stats.available}</h3>
            <p>C√≥ s·∫µn</p>
          </div>
        </div>
        
        <div className="stat-card">
          <div className="stat-icon">‚ùå</div>
          <div className="stat-info">
            <h3>{stats.outOfStock}</h3>
            <p>H·∫øt h√†ng</p>
          </div>
        </div>
      </div>

      {/* Filters */}
      <div className="filters-section">
        <div className="filters-row">
          <div className="filter-group">
            <input
              type="text"
              name="search"
              placeholder="T√¨m t√™n ƒë·ªì u·ªëng..."
              value={filters.search}
              onChange={handleFilterChange}
              className="search-input"
            />
          </div>

          <div className="filter-group">
            <select
              name="category"
              value={filters.category}
              onChange={handleFilterChange}
              className="filter-select"
            >
              {Object.entries(categories).map(([value, label]) => (
                <option key={value} value={value}>
                  {label}
                </option>
              ))}
            </select>
          </div>

          <div className="filter-group">
            <select
              name="isAvailable"
              value={filters.isAvailable}
              onChange={handleFilterChange}
              className="filter-select"
            >
              <option value="">T·∫•t c·∫£ tr·∫°ng th√°i</option>
              <option value="true">C√≥ s·∫µn</option>
              <option value="false">H·∫øt h√†ng</option>
            </select>
          </div>
        </div>
      </div>

      {error && <div className="error-message">{error}</div>}

      {/* Drinks Table */}
      <div className="drinks-table-container">
        <table className="drinks-table">
          <thead>
            <tr>
              <th>H√¨nh ·∫£nh</th>
              <th>T√™n ƒë·ªì u·ªëng</th>
              <th>Lo·∫°i</th>
              <th>Gi√°</th>
              <th>Size</th>
              <th>Th·ªùi gian pha</th>
              <th>Tr·∫°ng th√°i</th>
              <th>Trang ch·ªß</th>
              <th>Ng√†y t·∫°o</th>
              <th>H√†nh ƒë·ªông</th>
            </tr>
          </thead>
          <tbody>
            {drinks.map(drink => (
              <tr key={drink._id}>
                <td>
                  <div className="drink-image-cell">
                    {drink.images && drink.images.length > 0 ? (
                      <img src={drink.images[0]} alt={drink.name} />
                    ) : (
                      <div className="placeholder">ü•§</div>
                    )}
                  </div>
                </td>
                <td>
                  <div className="drink-name-cell">
                    <strong>{drink.name}</strong>
                    <p>{drink.description.substring(0, 50)}...</p>
                  </div>
                </td>
                <td>{categories[drink.category]}</td>
                <td className="price-cell">{formatPrice(drink.price)}</td>
                <td>
                  <span className="size-badge">{drink.size}</span>
                </td>
                <td>{drink.preparationTime} ph√∫t</td>
                <td>
                  <button
                    onClick={() => toggleAvailability(drink._id, drink.isAvailable)}
                    className={`status-btn ${drink.isAvailable ? 'available' : 'unavailable'}`}
                  >
                    {drink.isAvailable ? '‚úÖ C√≥ s·∫µn' : '‚ùå H·∫øt h√†ng'}
                  </button>
                </td>
                <td>
                  <span className={`featured-badge ${drink.isFeatured ? 'featured' : 'not-featured'}`}>
                    {drink.isFeatured ? 'üìå ƒê√£ ghim' : 'üìç Ch∆∞a ghim'}
                  </span>
                </td>
                <td>{formatDate(drink.createdAt)}</td>
                <td>
                  <div className="action-buttons">
                    <Link
                      to={`/products/${drink._id}`}
                      className="action-btn view"
                      title="Xem chi ti·∫øt"
                      target="_blank"
                    >
                      üëÅÔ∏è
                    </Link>
                    <button
                      onClick={() => toggleFeatured(drink._id, drink.isFeatured)}
                      className={`action-btn featured ${drink.isFeatured ? 'featured-active' : ''}`}
                      title={drink.isFeatured ? "B·ªè ghim kh·ªèi trang ch·ªß" : "Ghim v√†o trang ch·ªß"}
                    >
                      {drink.isFeatured ? 'üìå' : 'üìç'}
                    </button>
                    <Link
                      to={`/admin/drinks/edit/${drink._id}`}
                      className="action-btn edit"
                      title="Ch·ªânh s·ª≠a"
                    >
                      ‚úèÔ∏è
                    </Link>
                    <button
                      onClick={() => handleDeleteDrink(drink._id, drink.name)}
                      className="action-btn delete"
                      title="X√≥a"
                    >
                      üóëÔ∏è
                    </button>
                  </div>
                </td>
              </tr>
            ))}
          </tbody>
        </table>
      </div>

      {drinks.length === 0 && !loading && (
        <div className="no-drinks">
          <p>Kh√¥ng c√≥ ƒë·ªì u·ªëng n√†o</p>
          <Link to="/admin/drinks/add" className="add-first-btn">
            Th√™m ƒë·ªì u·ªëng ƒë·∫ßu ti√™n
          </Link>
        </div>
      )}

      {/* Pagination */}
      {pagination.pages > 1 && (
        <div className="pagination">
          <button
            onClick={() => handlePageChange(pagination.page - 1)}
            disabled={pagination.page === 1}
            className="pagination-btn"
          >
            ‚Üê Tr∆∞·ªõc
          </button>
          
          <span className="pagination-info">
            Trang {pagination.page} / {pagination.pages} 
            (T·ªïng: {pagination.total} ƒë·ªì u·ªëng)
          </span>
          
          <button
            onClick={() => handlePageChange(pagination.page + 1)}
            disabled={pagination.page === pagination.pages}
            className="pagination-btn"
          >
            Sau ‚Üí
          </button>
        </div>
      )}
    </div>
  );
};

export default DrinkManagement;
